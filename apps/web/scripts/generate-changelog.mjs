#!/usr/bin/env node
/**
 * Generates `src/data/changelog.generated.ts` from git history. Runs as the
 * first step of the web `build` script.
 *
 * The public Changelog page reads the committed snapshot, so the build always
 * has data. On a full clone (local dev, or CI with `fetch-depth: 0`) this
 * refreshes the snapshot from the latest history. On a shallow clone (the
 * default PR CI checkout) or when git is unavailable, it leaves the committed
 * file untouched — never clobbering it with a truncated 1-commit history.
 */
import { execFileSync } from 'node:child_process';
import { existsSync, mkdirSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const HERE = dirname(fileURLToPath(import.meta.url));
const OUT = resolve(HERE, '../src/data/changelog.generated.ts');
const MAX_COMMITS = 200;
const FIELD = '\x1f'; // unit separator between fields
const RECORD = '\x1e'; // record separator between commits

function git(args) {
  return execFileSync('git', args, { encoding: 'utf8', stdio: ['ignore', 'pipe', 'ignore'] }).trim();
}

function isShallowOrNoGit() {
  try {
    return git(['rev-parse', '--is-shallow-repository']) === 'true';
  } catch {
    return true; // not a git repo here (e.g. building from a tarball)
  }
}

function repoUrl() {
  try {
    const url = git(['remote', 'get-url', 'origin']).replace(/\.git$/, '');
    const m = url.match(/github\.com[:/]([^/]+\/[^/]+)$/);
    return m ? `https://github.com/${m[1]}` : null;
  } catch {
    return null;
  }
}

function readCommits() {
  const raw = git([
    'log',
    '--first-parent',
    '-n',
    String(MAX_COMMITS),
    `--format=%H${FIELD}%cI${FIELD}%s${RECORD}`,
    'HEAD',
  ]);
  return raw
    .split(RECORD)
    .map((r) => r.trim())
    .filter(Boolean)
    .map((r) => {
      const [hash, dateISO, subject] = r.split(FIELD);
      return { hash, dateISO, subject };
    });
}

function render(commits, url) {
  return (
    `/* eslint-disable */\n` +
    `// AUTO-GENERATED by scripts/generate-changelog.mjs — do not edit by hand.\n` +
    `// Refresh with: pnpm --filter @coursewise/web changelog\n\n` +
    `export interface RawCommit {\n  hash: string;\n  dateISO: string;\n  subject: string;\n}\n\n` +
    `export const REPO_URL: string | null = ${JSON.stringify(url)};\n\n` +
    `export const RAW_COMMITS: RawCommit[] = ${JSON.stringify(commits, null, 2)};\n`
  );
}

function main() {
  if (isShallowOrNoGit() && existsSync(OUT)) {
    console.log('[changelog] shallow clone / no git history — keeping committed snapshot.');
    return;
  }
  let commits;
  try {
    commits = readCommits();
  } catch (err) {
    if (existsSync(OUT)) {
      console.warn('[changelog] git log failed — keeping committed snapshot:', err?.message);
      return;
    }
    commits = [];
  }
  mkdirSync(dirname(OUT), { recursive: true });
  writeFileSync(OUT, render(commits, repoUrl()));
  console.log(`[changelog] wrote ${commits.length} commits to ${OUT}`);
}

main();
